""" useful to return a single string from a string list. This is a cleaner solution """

s = ['sqlite', 'is', 'an', 'inbuilt', 'python', 'database']
print(' '.join(s))  # returns sqlite is an inbuilt python database
p = ['python', 'is', 'an', 'interpreted', 'language']
print(' '.join(p))  # returns python is an interpreted language
